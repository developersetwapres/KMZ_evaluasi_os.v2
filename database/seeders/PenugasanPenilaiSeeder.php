                    'nip'  => $item['idPenilai'],
                    'type' => $item['type'],
                ]);
                continue;
            }

            /**
             * ===============================
             * 5. Insert / Update (IDEMPOTENT)
             * ===============================
             */
            PenugasanPenilai::updateOrCreate(
                [
                    'siklus_id'       => $siklus->id,
                    'outsourcing_id' => $outsourcing->id,
                    'tipe_penilai'   => $item['type'],
                ],
                [
                    'uuid'          => Str::uuid(),
                    'bobot_skor_id' => $bobotSkor->id,
                    'penilai_id'    => $userPenilai->id,
                    'status'        => 'incomplete',
                ]
            );
        }
    }
